function suma (numero1, numero2){
    return resultado = parseFloat(numero1) + parseFloat(numero2);
}

function resta (numero1, numero2){
    return resultado = parseFloat(numero1) - parseFloat(numero2);
}

function multiplicacion (numero1, numero2){
    return resultado = parseFloat(numero1) * parseFloat(numero2);
}

function division (numero1, numero2){
    return resultado = parseFloat(numero1) / parseFloat(numero2);
}

